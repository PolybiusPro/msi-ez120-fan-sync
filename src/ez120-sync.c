static int send_payload(const char *devnode, const unsigned char *payload, size_t index) {
    int fd = open(devnode, O_RDWR);
    if (fd < 0) {
        return -1;
    }

    unsigned char report[PAYLOAD_LEN];
    memcpy(report, payload, PAYLOAD_LEN);

    int rc = ioctl(fd, HIDIOCSFEATURE(PAYLOAD_LEN), report);
    if (rc < 0) {
        fprintf(stderr, "HIDIOCSFEATURE failed on %s (payload %zu): %s\n",
                devnode, index + 1, strerror(errno));
        close(fd);
        return -1;
    }

    close(fd);
    return 0;
}

static int send_feature_reports(const char *devnode) {
    const size_t payload_count = sizeof(payloads) / sizeof(payloads[0]);

    for (size_t i = 0; i < payload_count; i++) {
        if (send_payload(devnode, payloads[i], i) < 0) {
            return -1;
        }
        if (i + 1 < payload_count) {
            usleep(POST_PAYLOAD_US);
        }
    }

    return 0;
}

int main(void) {
    glob_t g;
    memset(&g, 0, sizeof(g));

    int grc = glob("/dev/hidraw*", 0, NULL, &g);
    if (grc != 0) {
        fprintf(stderr, "No hidraw devices found\n");
        return 1;
    }

    int found = 0;

    for (size_t i = 0; i < g.gl_pathc; i++) {
        const char *path = g.gl_pathv[i];
        int fd = open(path, O_RDWR);
        if (fd < 0) {
            continue;
        }

        struct hidraw_devinfo info;
        memset(&info, 0, sizeof(info));

        if (ioctl(fd, HIDIOCGRAWINFO, &info) < 0) {
            close(fd);
            continue;
        }

        if (info.vendor == VENDOR_ID && info.product == PRODUCT_ID) {
            close(fd);
            printf("Found target device: %s (%04x:%04x)\n", path, info.vendor, info.product);
            if (send_feature_reports(path) == 0) {
                found = 1;
                break;
            }
        } else {
            close(fd);
        }
    }

    globfree(&g);

    if (!found) {
        fprintf(stderr, "Target HID device %04x:%04x not found or send failed\n", VENDOR_ID, PRODUCT_ID);
        return 1;
    }

    return 0;
}
